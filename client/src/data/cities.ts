/** Fuzzy-ish prefix + substring search, scored for relevance. */
export function searchCities(query: string, limit = 12): City[] {
  const q = query.trim().toLowerCase();
  if (q.length < 1) return [];
  const results: { c: City; score: number }[] = [];
  for (const c of CITIES) {
    const n = c.name.toLowerCase();
    const a = c.admin.toLowerCase();
    let score = 0;
    if (n === q) score = 100;
    else if (n.startsWith(q)) score = 80;
    else if (n.includes(q)) score = 50;
    else if (a.startsWith(q)) score = 30;
    else if (a.includes(q)) score = 10;
    if (score > 0) results.push({ c, score });
  }
  results.sort((a, b) => b.score - a.score || a.c.name.localeCompare(b.c.name));
  return results.slice(0, limit).map((r) => r.c);
}
